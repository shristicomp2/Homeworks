public class square
{
    void prnsqr(int i)
    {
         System.out.println("Integer "+i+"square is "+i*i);
    }
    void prnsqr(char c)
    {
         System.out.println(c+ " is a character. "+"Thus no square.");
    }
    void pnrsqr(float f)
    {
        System.out.println("Float "+f+"square is "+f*f);
    }
    void pnrsqr(double d)
    {
        System.out.println("Double "+d+"square is "+d*d);
    }
}